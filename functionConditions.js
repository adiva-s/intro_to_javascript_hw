function sortingHat(house){
    if (house == "hufflepuff"){
        alert("You might belong in Hufflepuff, where they are just and loyal");
    }
    else if (house == "ravenclaw"){
        alert("Or yet in wise old Ravenclaw, if you've a ready mind, where those of wit and learning, will always find their kind");
    }
    else if (house == "gryffindor"){
        alert("Gryffindor, where dwell the brave at heart!");
    }
    else if (house == "slytherin"){
        alert("Or perhaps in Slytherin, you'll make your real friends, those cunning folk use any means, to achieve their ends");
    }
    else {
        alert("Invalid House!");
    }
}

sortingHat("hufflepuff");
sortingHat("ravenclaw");
sortingHat("gryffindor");
sortingHat("slytherin");
sortingHat("something");